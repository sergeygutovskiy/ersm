	<script>
		var isCategoriesActive = false;
		var categoriesEl = document.getElementsByClassName("prices-links")[0];

		function toogleCategories(el) 
		{
			if (!isCategoriesActive)
			{
				categoriesEl.classList.add("prices-links--active");
				el.classList.add("prices-links-title--active");
			}
			else 
			{
				categoriesEl.classList.remove("prices-links--active");
				el.classList.remove("prices-links-title--active");
			}

			isCategoriesActive = !isCategoriesActive;
		}
	</script>

